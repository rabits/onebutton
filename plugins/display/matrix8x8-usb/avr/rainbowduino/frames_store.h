void displayStoredRGBFrame(uint8_t frame, uint8_t brightness) {
  for( uint8_t x=0; x<8; x++ ) {
    for( uint8_t y=0; y<8; y++ ) {
      uint8_t pos = x*8+y;
      uint8_t rgb[] = {
        pgm_read_byte(frames_rgb_store[frame][0] + pos),
        pgm_read_byte(frames_rgb_store[frame][1] + pos),
        pgm_read_byte(frames_rgb_store[frame][2] + pos)
      };
      bright(rgb, brightness);
      Rb.setPixelXY(x, y, rgb[0], rgb[1], rgb[2]);
    }
  }
}

